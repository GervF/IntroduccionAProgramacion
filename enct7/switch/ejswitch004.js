// 4. Se pide ingresar una letra del alfabeto y mostrar
// si dicha letra es vocal o consonante.

let letra = prompt("Ingrese una letra del alfabeto y le digo si es vocal o consonante").toUpperCase();
switch (letra) {
  case "A":
    alert("Es una vocal");
    break;
  case "E":
    alert("Es una vocal");
    break;
  case "I":
    alert("Es una vocal");
    break;
  case "O":
    alert("Es una vocal");
    break;
  case "U":
    alert("Es una vocal");
    break;
  default:
    alert("Es una consonante");
    break;
}
